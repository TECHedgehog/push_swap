#include "libft.h"

char	*ft_itoa(int nb)
{
	int			j;
	long int	l;
	long int	n;
	char		*num;

	n = nb;
	j = (n > 0) ? 0 : 1;
	l = (n < 0) ? n * -1 : n;
	while (l > 0)
		l = j++ ? l / 10 : l / 10;
	l = (n < 0) ? n * -1 : n;
	num = malloc(j + 1);
	if (!num)
		return (0);
	num[j] = '\0';
	num[0] = (n == 0) ? '0' : '0';
	while (l > 0)
	{
		num[j - 1] = l % 10 + 48;
		l = j-- ? l / 10 : l / 10;
	}
	(n < 0) ? num[0] = '-' : 0;
	return (num);
}
